//package STUD;

public class Student {
private int rollNo;
private String name;
private int markPhy;
private int markChem;
private int markBio;

public int getRollNo() {
	return rollNo;
}
public void setRollNo(int rollNo) {
	this.rollNo = rollNo;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getMarkPhy() {
	return markPhy;
}
public void setMarkPhy(int markPhy) {
	this.markPhy = markPhy;
}
public int getMarkChem() {
	return markChem;
}
public void setMarkChem(int markChem) {
	this.markChem = markChem;
}
public int getMarkBio() {
	return markBio;
}
public void setMarkBio(int markBio) {
	this.markBio = markBio;
}


}
